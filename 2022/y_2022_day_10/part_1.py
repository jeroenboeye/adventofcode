INPUT_TXT = Path(__file__).parent / "input.txt"


def solve(text: str) -> int:
    """Solve the puzzle."""
    cycles_to_check = [20, 60, 100, 140, 180, 220]
    signal_strength = 0
    cycle = 0
    x = 1
    for line in text.splitlines():
        if line == "noop":
            cycle += 1
            if cycle in cycles_to_check:
                signal_strength += cycle * x
        else:
            _, value = line.split()
            cycle += 1
            if cycle in cycles_to_check:
                signal_strength += cycle * x
            cycle += 1
            if cycle in cycles_to_check:
                signal_strength += cycle * x
            x += int(value)
    return signal_strength


